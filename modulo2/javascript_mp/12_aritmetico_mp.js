const precioPlato = 15.99;
const cantidad = 3;

// Operaciones básicas del menú
console.log(precioPlato + 2.50);   // 18.49  — precio con propina
console.log(precioPlato - 2.00);   // 13.99  — precio con descuento
console.log(precioPlato * cantidad);   // 47.97  — total para 3 platos
console.log(precioPlato / cantidad);   // 5.33  — precio por porción
console.log(100 % cantidad);   // 1   — resto de dividir 100 entre 3