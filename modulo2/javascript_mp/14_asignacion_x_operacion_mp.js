let totalCuenta = 10;

totalCuenta += 5;    // totalCuenta = totalCuenta + 5  → 15 (agregar propina)
totalCuenta -= 3;    // totalCuenta = totalCuenta - 3  → 12 (descuento)
totalCuenta *= 2;    // totalCuenta = totalCuenta * 2  → 24 (para 2 personas)
totalCuenta /= 4;    // totalCuenta = totalCuenta / 4  → 6 (dividir entre 4)
totalCuenta %= 5;    // totalCuenta = totalCuenta % 5  → 1 (resto para propina)
totalCuenta **= 2;   // totalCuenta = totalCuenta ** 2 → 1 (no tiene sentido, pero ejemplo)