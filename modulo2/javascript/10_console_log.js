// console.log — salida estándar
console.log("Mensaje normal");
console.log("Valor:", 42);
console.log("Múltiples:", "uno", "dos", "tres");

// console.error — salida de error (en rojo en la consola)
console.error("Algo salió mal");

// console.warn — advertencia (en amarillo)
console.warn("Atención: valor inesperado");

// console.table — muestra arrays y objetos como tabla
console.table([{ nombre: "Ana", edad: 28 }, { nombre: "Luis", edad: 31 }]);

// console.log con formato
const x = 42;
const nombre = "Ana";
console.log(`x = ${x}, nombre = ${nombre}`);