const a = 10;
const b = 3;

// Operaciones básicas
console.log(a + b);   // 13  — suma
console.log(a - b);   // 7   — resta
console.log(a * b);   // 30  — multiplicación
console.log(a / b);   // 3.3333...  — división (siempre devuelve decimal si no es exacta)
console.log(a % b);   // 1   — módulo (resto de la división entera)
console.log(a ** b);  // 1000 — potencia (ES2016) — equivale a Math.pow(a, b)